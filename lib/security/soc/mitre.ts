  /**
   * Remove duplicate TTP mappings and merge evidence
   */
  private deduplicateTTPs(mappings: Array<{
    technique: MITRETechnique;
    confidence: number;
    evidence: string[];
    subTechniques?: string[];
  }>): Array<{
    technique: MITRETechnique;
    confidence: number;
    evidence: string[];
    subTechniques?: string[];
  }> {
    const seen = new Map<string, {
      technique: MITRETechnique;
      confidence: number;
      evidence: string[];
      subTechniques?: string[];
    }>();

    for (const mapping of mappings) {
      const key = mapping.technique.id;
      const existing = seen.get(key);
      
      if (!existing || mapping.confidence > existing.confidence) {
        const mergedSubTechniques = [
          ...(existing?.subTechniques || []),
          ...(mapping.subTechniques || []),
        ];
        
        seen.set(key, {
          ...mapping,
          evidence: existing ? 
            [...existing.evidence, ...mapping.evidence] : 
            mapping.evidence,
          subTechniques: [...new Set(mergedSubTechniques)],
        });
      }
    }

    return Array.from(seen.values());
  }
