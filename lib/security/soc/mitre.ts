  /**
   * Analyze security event and map to MITRE ATT&CK framework
   */
  async analyzeEvent(event: any): Promise<MITREMapping> {
    const mapping: MITREMapping = {
      tactics: [],
      techniques: [],
      confidence: 0
    };

    // Pattern-based analysis
    const patternMatches = this.analyzeEventPatterns(event);
    if (patternMatches.length > 0) {
      patternMatches.forEach(match => {
        mapping.tactics.push(...match.tactics);
        mapping.techniques.push(...match.techniques);
        mapping.confidence = Math.max(mapping.confidence, match.confidence);
      });
    }

    // Behavioral analysis
    const behaviorMapping = this.analyzeBehavior(event);
    if (behaviorMapping) {
      mapping.tactics.push(...behaviorMapping.tactics);
      mapping.techniques.push(...behaviorMapping.techniques);
      mapping.confidence = Math.max(mapping.confidence, behaviorMapping.confidence);
    }

    // Remove duplicates
    mapping.tactics = [...new Set(mapping.tactics)];
    mapping.techniques = [...new Set(mapping.techniques)];

    return mapping;
  }

  /**
   * Get technique details by ID
   */
  getTechnique(techniqueId: string): MITRETechnique | null {
    return this.techniques.get(techniqueId) || null;
  }

  /**
   * Get tactic details by ID
   */
  getTactic(tacticId: string): MITRETactic | null {
    return this.tactics.get(tacticId) || null;
  }

  /**
   * Get all techniques for a tactic
   */
  getTechniquesForTactic(tacticId: string): MITRETechnique[] {
    const tactic = this.tactics.get(tacticId);
    if (!tactic) return [];

    return tactic.techniques
      .map(techId => this.techniques.get(techId))
      .filter(tech => tech !== undefined) as MITRETechnique[];
  }

  /**
   * Search techniques by name or description
   */
  searchTechniques(query: string): MITRETechnique[] {
    const lowercaseQuery = query.toLowerCase();
    return Array.from(this.techniques.values()).filter(tech =>
      tech.name.toLowerCase().includes(lowercaseQuery) ||
      tech.description.toLowerCase().includes(lowercaseQuery)
    );
  }

