/**
 * MITRE ATT&CK Heat Map Generator for SOC Dashboard
 */
export class MitreHeatMapGenerator {
  private mitreEngine: MITREAttackEngine;

  constructor(mitreEngine: MITREAttackEngine) {
    this.mitreEngine = mitreEngine;
  }

  /**
   * Generate comprehensive heat map from TTP findings
   */
  generateHeatMap(
    findings: Array<{
      ttp: string;
      risk: number;
      timestamp: Date;
      subTechniques?: string[];
      confidence?: number;
    }>,
    timeRangeHours = 24
  ): TtpHeatMap[] {
    const cutoffTime = new Date(Date.now() - timeRangeHours * 60 * 60 * 1000);
    
    // Filter recent findings
    const recentFindings = findings.filter(f => f.timestamp > cutoffTime);
    
    // Group by TTP
    const ttpGroups = new Map<string, Array<typeof findings[0]>>();
    
    for (const finding of recentFindings) {
      if (!ttpGroups.has(finding.ttp)) {
        ttpGroups.set(finding.ttp, []);
      }
      ttpGroups.get(finding.ttp)!.push(finding);
    }

    // Generate heat map entries
    const heatMap: TtpHeatMap[] = [];
    
    for (const [ttp, ttpFindings] of ttpGroups) {
      const technique = this.mitreEngine.getTechnique(ttp);
      if (!technique) continue;

      const count = ttpFindings.length;
      const avgRisk = ttpFindings.reduce((sum, f) => sum + f.risk, 0) / count;
      const avgConfidence = ttpFindings.reduce((sum, f) => sum + (f.confidence || 0.5), 0) / count;
      const lastSeen = new Date(Math.max(...ttpFindings.map(f => f.timestamp.getTime())));

      // Calculate composite score (risk + confidence + frequency)
      const frequency = count / 24; // events per hour
      const compositeScore = (avgRisk * 0.4) + (avgConfidence * 0.3) + Math.min(frequency * 0.3, 1.0);

      heatMap.push({
        tactic: technique.tactic[0] || 'Unknown',
        technique: technique.name,
        ttp: technique.id,
        count,
        avgRisk: avgRisk,
        lastSeen,
        compositeScore,
        avgConfidence,
        frequency,
      } as TtpHeatMap);
    }

    return heatMap.sort((a, b) => (b.compositeScore || 0) - (a.compositeScore || 0));
  }

  /**
   * Generate matrix view optimized for SOC dashboard
   */
  generateMatrix(heatMap: TtpHeatMap[]): Array<{
    tactic: string;
    tacticId: string;
    techniques: Array<{
      id: string;
      name: string;
      count: number;
      avgRisk: number;
      intensity: number; // 0-100 for color mapping
      lastSeen: Date;
      compositeScore: number;
      threatLevel: 'low' | 'medium' | 'high' | 'critical';
      subTechniques?: string[];
    }>;
    tacticThreatLevel: 'low' | 'medium' | 'high' | 'critical';
    tacticCount: number;
  }> {
    const tactics = this.mitreEngine.getAllTactics();
    const matrix: Array<{
      tactic: string;
      tacticId: string;
      techniques: Array<{
        id: string;
        name: string;
        count: number;
        avgRisk: number;
        intensity: number;
        lastSeen: Date;
        compositeScore: number;
        threatLevel: 'low' | 'medium' | 'high' | 'critical';
        subTechniques?: string[];
      }>;
      tacticThreatLevel: 'low' | 'medium' | 'high' | 'critical';
      tacticCount: number;
    }> = [];

    // Find max values for normalization
    const maxCount = Math.max(...heatMap.map(h => h.count), 1);
    const maxComposite = Math.max(...heatMap.map(h => h.compositeScore || 0), 1);

    for (const tactic of tactics) {
      const tacticTechniques = heatMap
        .filter(h => h.tactic === tactic.name)
        .map(h => {
          const intensity = Math.round((h.compositeScore || 0) / maxComposite * 100);
          const threatLevel = this.calculateThreatLevel(h.compositeScore || 0, h.avgRisk);
          
          return {
            id: h.ttp,
            name: h.technique,
            count: h.count,
            avgRisk: h.avgRisk,
            intensity,
            lastSeen: h.lastSeen,
            compositeScore: h.compositeScore || 0,
            threatLevel,
            subTechniques: [], // Could be populated from findings
          };
        });

      const tacticCount = tacticTechniques.reduce((sum, t) => sum + t.count, 0);
      const tacticMaxThreat = tacticTechniques.reduce((max, t) => {
        const levels = ['low', 'medium', 'high', 'critical'];
        const maxIndex = Math.max(levels.indexOf(max), levels.indexOf(t.threatLevel));
        return levels[maxIndex] as 'low' | 'medium' | 'high' | 'critical';
      }, 'low' as 'low' | 'medium' | 'high' | 'critical');

      matrix.push({
        tactic: tactic.name,
        tacticId: tactic.id,
        techniques: tacticTechniques,
        tacticThreatLevel: tacticMaxThreat,
        tacticCount,
      });
    }

    return matrix.sort((a, b) => {
      const threatOrder = ['low', 'medium', 'high', 'critical'];
      return threatOrder.indexOf(b.tacticThreatLevel) - threatOrder.indexOf(a.tacticThreatLevel);
    });
  }

  /**
   * Calculate threat level based on composite score and risk
   */
  private calculateThreatLevel(compositeScore: number, avgRisk: number): 'low' | 'medium' | 'high' | 'critical' {
    if (compositeScore >= 0.8 || avgRisk >= 0.9) return 'critical';
    if (compositeScore >= 0.6 || avgRisk >= 0.7) return 'high';
    if (compositeScore >= 0.4 || avgRisk >= 0.5) return 'medium';
    return 'low';
  }

  /**
   * Generate trend data for time series analysis
   */
  generateTrendData(
    findings: Array<{
      ttp: string;
      risk: number;
      timestamp: Date;
    }>,
    timeRangeHours = 24,
    intervalMinutes = 60
  ): Array<{
    timestamp: Date;
    tacticCounts: Record<string, number>;
    totalEvents: number;
    avgRisk: number;
    topTechniques: string[];
  }> {
    const cutoffTime = new Date(Date.now() - timeRangeHours * 60 * 60 * 1000);
    const intervalMs = intervalMinutes * 60 * 1000;
    
    const recentFindings = findings.filter(f => f.timestamp > cutoffTime);
    const trends: Array<{
      timestamp: Date;
      tacticCounts: Record<string, number>;
      totalEvents: number;
      avgRisk: number;
      topTechniques: string[];
    }> = [];

    // Create time buckets
    for (let time = cutoffTime.getTime(); time < Date.now(); time += intervalMs) {
      const bucketStart = new Date(time);
      const bucketEnd = new Date(time + intervalMs);
      
      const bucketFindings = recentFindings.filter(f => 
        f.timestamp >= bucketStart && f.timestamp < bucketEnd
      );

      const tacticCounts: Record<string, number> = {};
      const techniqueCounts: Record<string, number> = {};

      for (const finding of bucketFindings) {
        const technique = this.mitreEngine.getTechnique(finding.ttp);
        if (technique) {
          const tactic = technique.tactic[0] || 'Unknown';
          tacticCounts[tactic] = (tacticCounts[tactic] || 0) + 1;
          techniqueCounts[finding.ttp] = (techniqueCounts[finding.ttp] || 0) + 1;
        }
      }

      const avgRisk = bucketFindings.length > 0 
        ? bucketFindings.reduce((sum, f) => sum + f.risk, 0) / bucketFindings.length
        : 0;

      const topTechniques = Object.entries(techniqueCounts)
        .sort(([,a], [,b]) => b - a)
        .slice(0, 5)
        .map(([ttp]) => ttp);

      trends.push({
        timestamp: bucketStart,
        tacticCounts,
        totalEvents: bucketFindings.length,
        avgRisk,
        topTechniques,
      });
    }

    return trends;
  }
}

