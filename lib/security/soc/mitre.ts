  /**
   * Pattern-based event analysis
   */
  private analyzeEventPatterns(event: any): MITREMapping[] {
    const matches: MITREMapping[] = [];

    // Web application attacks
    if (event.type === 'web_attack') {
      if (event.normalized?.url?.includes('sql') || 
          event.raw?.payload?.match(/(union|select|insert|delete|update|drop)/i)) {
        matches.push({
          tactics: ['TA0001'], // Initial Access
          techniques: ['T1190'], // Exploit Public-Facing Application
          confidence: 0.8,
          description: 'SQL Injection attempt detected'
        });
      }

      if (event.raw?.payload?.match(/(<script|javascript:|onload|onerror)/i)) {
        matches.push({
          tactics: ['TA0001'], // Initial Access
          techniques: ['T1190'], // Exploit Public-Facing Application
          confidence: 0.7,
          description: 'Cross-Site Scripting attempt detected'
        });
      }

      if (event.normalized?.url?.match(/(\.\.\/|\.\.\\|%2e%2e%2f)/i)) {
        matches.push({
          tactics: ['TA0007'], // Discovery
          techniques: ['T1083'], // File and Directory Discovery
          confidence: 0.6,
          description: 'Directory traversal attempt detected'
        });
      }
    }

    // Authentication attacks
    if (event.type === 'authentication_failed') {
      matches.push({
        tactics: ['TA0006'], // Credential Access
        techniques: ['T1110'], // Brute Force
        confidence: 0.5,
        description: 'Authentication failure - possible brute force'
      });
    }

    // Network reconnaissance
    if (event.type === 'port_scan' || event.type === 'network_scan') {
      matches.push({
        tactics: ['TA0007'], // Discovery
        techniques: ['T1046'], // Network Service Scanning
        confidence: 0.7,
        description: 'Network scanning activity detected'
      });
    }

    // Data exfiltration
    if (event.type === 'large_data_transfer' || event.type === 'unusual_upload') {
      matches.push({
        tactics: ['TA0010'], // Exfiltration
        techniques: ['T1041'], // Exfiltration Over C2 Channel
        confidence: 0.6,
        description: 'Potential data exfiltration detected'
      });
    }

    // Persistence attempts
    if (event.type === 'file_modification' && 
        event.normalized?.url?.match(/(autostart|startup|cron|systemd)/i)) {
      matches.push({
        tactics: ['TA0003'], // Persistence
        techniques: ['T1543'], // Create or Modify System Process
        confidence: 0.7,
        description: 'Persistence mechanism modification detected'
      });
    }

    return matches;
  }

  /**
   * Behavioral analysis of events
   */
  private analyzeBehavior(event: any): MITREMapping | null {
    // Analyze event behavior patterns
    const suspicious = this.calculateSuspicionScore(event);
    
    if (suspicious > 0.7) {
      return {
        tactics: ['TA0005'], // Defense Evasion
        techniques: ['T1070'], // Indicator Removal on Host
        confidence: suspicious,
        description: 'Suspicious behavior pattern detected'
      };
    }

    return null;
  }

  /**
   * Calculate suspicion score based on event characteristics
   */
  private calculateSuspicionScore(event: any): number {
    let score = 0;

    // Unusual time patterns
    const hour = new Date(event.timestamp).getHours();
    if (hour < 6 || hour > 22) {
      score += 0.2; // Activity during off-hours
    }

    // Geographic anomalies
    if (event.normalized?.geolocation?.country && 
        !['US', 'GB', 'DE', 'FR', 'CA', 'AU'].includes(event.normalized.geolocation.country)) {
      score += 0.3; // Activity from uncommon countries
    }

    // User agent anomalies
    if (event.normalized?.userAgent) {
      if (event.normalized.userAgent.includes('bot') || 
          event.normalized.userAgent.includes('crawler') ||
          event.normalized.userAgent.includes('scanner')) {
        score += 0.4;
      }
    }

    // High frequency from same source
    // This would require historical data analysis
    
    return Math.min(score, 1.0);
  }

